#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*tmp;

	if (!lst || !f)
		return ;
	tmp = lst->next;
	f(lst);
	while (tmp)
	{
		lst = tmp;
		tmp = lst->next;
		f(lst);
	}
}
