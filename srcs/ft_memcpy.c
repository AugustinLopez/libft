#include <string.h>

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*sdst;
	char	*ssrc;

	if (!dst && !src)
		return (0);
	sdst = (char*)dst;
	ssrc = (char*)src;
	while (n--)
		*(sdst++) = *(ssrc++);
	return (dst);
}
