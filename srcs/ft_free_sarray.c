#include <stdlib.h>
#include <string.h>

void	ft_free_sarray(char ***s)
{
	size_t	i;

	i = 1;
	if (s && *s)
	{
		while (**s && (*s)[i])
			free((*s)[i++]);
		free((*s)[0]);
		free(*s);
	}
	*s = 0;
}
