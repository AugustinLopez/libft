static double	dabs(double d)
{
	return (d * (1 - 2 * (d < 0)));
}

double			ft_sqrt(unsigned int i)
{
	double d1;
	double d2;

	d1 = i / 2 + 0.0;
	d2 = (d1 + (i / d1)) / 2 + 0.0;
	while (dabs(d1 - d2) >= 0.0000000001)
	{
		d1 = d2;
		d2 = (d1 + (i / d1)) / 2;
	}
	return (d2);
}
