int	ft_iswhitespace(char *s, int ignore_line_feed)
{
	int	i;

	i = 10 + (ignore_line_feed != 0);
	if (!s)
		return (1);
	while (*s)
	{
		if (*s != ' ' && *s != '\t' && (!(*s >= i && *s <= 13)))
			return (0);
		s++;
	}
	return (1);
}
